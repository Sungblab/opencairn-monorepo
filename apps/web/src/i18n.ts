import { getRequestConfig } from "next-intl/server";
import { notFound } from "next/navigation";
import { defaultLocale, locales, type Locale } from "./i18n-locales";

export {
  defaultLocale,
  localeNames,
  locales,
  type Locale,
} from "./i18n-locales";

export default getRequestConfig(async ({ requestLocale }) => {
  const requested = (await requestLocale) ?? defaultLocale;
  if (!locales.includes(requested as Locale)) notFound();
  const locale = requested as Locale;

  const [
    common,
    landing,
    dashboard,
    sidebar,
    app,
    editor,
    auth,
    collab,
    importMessages,
    onboarding,
    appShell,
    agentPanel,
    research,
    graph,
    learn,
    literature,
    canvas,
    note,
    project,
    workspaceSettings,
    account,
    palette,
    notifications,
    settings,
    chat,
    chatScope,
    publicShare,
    shareDialog,
    ingest,
    docEditor,
    agents,
    synthesisExport,
    accountNotifications,
    noteHistory,
    agentFiles,
    codeWorkspaces,
    admin,
  ] = await Promise.all([
    import(`../messages/${locale}/common.json`).then((m) => m.default),
    import(`../messages/${locale}/landing.json`).then((m) => m.default),
    import(`../messages/${locale}/dashboard.json`).then((m) => m.default),
    import(`../messages/${locale}/sidebar.json`).then((m) => m.default),
    import(`../messages/${locale}/app.json`).then((m) => m.default),
    import(`../messages/${locale}/editor.json`).then((m) => m.default),
    import(`../messages/${locale}/auth.json`).then((m) => m.default),
    import(`../messages/${locale}/collab.json`).then((m) => m.default),
    import(`../messages/${locale}/import.json`).then((m) => m.default),
    import(`../messages/${locale}/onboarding.json`).then((m) => m.default),
    import(`../messages/${locale}/app-shell.json`).then((m) => m.default),
    import(`../messages/${locale}/agent-panel.json`).then((m) => m.default),
    import(`../messages/${locale}/research.json`).then((m) => m.default),
    import(`../messages/${locale}/graph.json`).then((m) => m.default),
    import(`../messages/${locale}/learn.json`).then((m) => m.default),
    import(`../messages/${locale}/literature.json`).then((m) => m.default),
    import(`../messages/${locale}/canvas.json`).then((m) => m.default),
    import(`../messages/${locale}/note.json`).then((m) => m.default),
    import(`../messages/${locale}/project.json`).then((m) => m.default),
    import(`../messages/${locale}/workspace-settings.json`).then(
      (m) => m.default,
    ),
    import(`../messages/${locale}/account.json`).then((m) => m.default),
    import(`../messages/${locale}/palette.json`).then((m) => m.default),
    import(`../messages/${locale}/notifications.json`).then((m) => m.default),
    import(`../messages/${locale}/settings.json`).then((m) => m.default),
    import(`../messages/${locale}/chat.json`).then((m) => m.default),
    import(`../messages/${locale}/chat-scope.json`).then((m) => m.default),
    import(`../messages/${locale}/public-share.json`).then((m) => m.default),
    import(`../messages/${locale}/share-dialog.json`).then((m) => m.default),
    import(`../messages/${locale}/ingest.json`).then((m) => m.default),
    import(`../messages/${locale}/doc-editor.json`).then((m) => m.default),
    import(`../messages/${locale}/agents.json`).then((m) => m.default),
    import(`../messages/${locale}/synthesis-export.json`).then(
      (m) => m.default,
    ),
    import(`../messages/${locale}/account-notifications.json`).then(
      (m) => m.default,
    ),
    import(`../messages/${locale}/note-history.json`).then((m) => m.default),
    import(`../messages/${locale}/agent-files.json`).then((m) => m.default),
    import(`../messages/${locale}/code-workspaces.json`).then((m) => m.default),
    import(`../messages/${locale}/admin.json`).then((m) => m.default),
  ]);

  return {
    locale,
    messages: {
      common,
      landing,
      dashboard,
      sidebar,
      app,
      editor,
      auth,
      collab,
      import: importMessages,
      onboarding,
      appShell,
      agentPanel,
      research,
      graph,
      learn,
      literature,
      canvas,
      note,
      project,
      workspaceSettings,
      account,
      palette,
      notifications,
      settings,
      chat,
      chatScope,
      publicShare,
      shareDialog,
      ingest,
      docEditor,
      agents,
      synthesisExport,
      accountNotifications,
      noteHistory,
      agentFiles,
      codeWorkspaces,
      admin,
    },
  };
});
